# Dynamic Typing

# Python is highly flexible language in respect to reassigning variables to different types
# not just from numbers to numbers or strings to strings
# but also from string to number or any data type to any other data type

number = 10
print(number)
number = "Hello World"
print(number)
number = True
print(number)
number = None
print(number)

# the opposite of dynamically types variable are static variable
# in which we fix the type of data that goes into the variable
